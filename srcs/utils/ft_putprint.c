#include "libft.h"

void	ft_putprint(char const *s)
{
	int		i;

	if (s)
	{
		i = 0;
		while (s[i] != 0 && ft_isprint(s[i]))
		{
			ft_putchar(s[i]);
			i++;
		}
	}
}
